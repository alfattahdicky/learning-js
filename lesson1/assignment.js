/**
   JavaScript Assignment
   	 Assignment operators assign values to JavaScript Variables
   	 	- adds value 
   	 	- substracts value
   	 	- multiplies value
   	 	- divides values
   	 	- remainder values
*/
let x = 0;

// Adds Value
x += 5; // 5

// Substract value
x -= 3; // 2

// multiplies value
x *= 5; // 10

// divides value
x /= 5; // 2

// remainder value
x %= 3; // 1



